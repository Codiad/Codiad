    //////////////////////////////////////////////////////////////////
    // Check If Path is absolute
    //////////////////////////////////////////////////////////////////
        
    public static function isAbsPath( $path ) {
        if ( preg_match('/^[A-Za-z]:\\/', $path) || $path[0] === '\\' || $path[0] === '/') {
            return true;
        } else {
            return false;
        }
    }
        
